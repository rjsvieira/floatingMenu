public enum MovementStyle {
    /**
     * The button cannot be dragged
     */
    ANCHORED,
    /**
     * The button can only be placed at the sides of the screen
     */
    STICKED_TO_SIDES,
    /**
     * The button can be placed anywhere
     */
    FREE
}
